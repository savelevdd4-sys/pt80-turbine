def h_water(P):
    """
    Энтальпия кипящей воды, кДж/кг
    
    Parameters:
    P : float - давление в МПа
    
    Returns:
    float - энтальпия воды на линии насыщения, кДж/кг
    """
    t = ts(P)
    # Для воды энтальпия примерно равна 4.19 * t, но с небольшой поправкой
    # для высоких давлений
    if P < 10:
        return 4.19 * t
    else:
        # При высоких давлениях теплоемкость немного меняется
        return 4.19 * t * (1 + 0.0001 * (P - 10))


def h_steam_sat(P):
    """
    Энтальпия сухого насыщенного пара, кДж/кг
    
    Parameters:
    P : float - давление в МПа
    
    Returns:
    float - энтальпия сухого насыщенного пара, кДж/кг
    """
    t = ts(P)
    # Базовая формула: 2500 + 1.88 * t, но с поправкой для высоких давлений
    if P < 5:
        return 2500 + 1.88 * t
    elif P < 10:
        return 2500 + 1.88 * t + 0.1 * (P - 5) * 50
    else:
        return 2500 + 1.88 * t + 0.2 * (P - 10) * 40


def h_steam(P, t):
    """
    Энтальпия перегретого пара, кДж/кг
    
    Parameters:
    P : float - давление в МПа
    t : float - температура в °C
    
    Returns:
    float - энтальпия перегретого пара, кДж/кг
    """
    tsat = ts(P)
    if t <= tsat:
        # Если температура ниже или равна температуре насыщения,
        # возвращаем энтальпию насыщенного пара
        return h_steam_sat(P)
    else:
        # Теплоемкость перегретого пара зависит от давления и температуры
        # В среднем 2.0-2.3 кДж/(кг·°C)
        cp = 2.1
        
        # Коррекция теплоемкости для высоких температур и давлений
        if P > 5 and t > 400:
            cp = 2.3
        elif P > 10:
            cp = 2.4
        
        return h_steam_sat(P) + cp * (t - tsat)


def h_wet_steam(P, x):
    """
    Энтальпия влажного пара, кДж/кг
    
    Parameters:
    P : float - давление в МПа
    x : float - степень сухости (0..1)
    
    Returns:
    float - энтальпия влажного пара, кДж/кг
    """
    h_water_val = h_water(P)
    h_steam_sat_val = h_steam_sat(P)
    return h_water_val + x * (h_steam_sat_val - h_water_val)


def h_water_temp(t):
    """
    Энтальпия воды по температуре (несжатая вода), кДж/кг
    
    Parameters:
    t : float - температура в °C
    
    Returns:
    float - энтальпия воды, кДж/кг
    """
    return 4.19 * t


def p_sat(t):
    """
    Давление насыщения по температуре (обратная функция к ts(P))
    
    Parameters:
    t : float - температура в °C
    
    Returns:
    float - давление насыщения, МПа
    """
    # Для диапазона конденсатора (до 100°C) используем существующую кусочно-линейную аппроксимацию
    if t <= 45.8:
        if t <= 24.1:
            return 0.003
        elif t <= 28.5:
            return 0.003 + 0.001 * (t - 24.1) / (28.5 - 24.1)
        elif t <= 32.9:
            return 0.004 + 0.001 * (t - 28.5) / (32.9 - 28.5)
        elif t <= 36.2:
            return 0.005 + 0.001 * (t - 32.9) / (36.2 - 32.9)
        elif t <= 39.0:
            return 0.006 + 0.001 * (t - 36.2) / (39.0 - 36.2)
        elif t <= 41.5:
            return 0.007 + 0.001 * (t - 39.0) / (41.5 - 39.0)
        elif t <= 43.8:
            return 0.008 + 0.001 * (t - 41.5) / (43.8 - 41.5)
        else:
            return 0.009 + 0.001 * (t - 43.8) / (45.8 - 43.8)
    elif t <= 60.1:
        return 0.01 + 0.01 * (t - 45.8) / (60.1 - 45.8)
    elif t <= 69.1:
        return 0.02 + 0.01 * (t - 60.1) / (69.1 - 60.1)
    elif t <= 75.9:
        return 0.03 + 0.01 * (t - 69.1) / (75.9 - 69.1)
    elif t <= 81.3:
        return 0.04 + 0.01 * (t - 75.9) / (81.3 - 75.9)
    elif t <= 85.9:
        return 0.05 + 0.01 * (t - 81.3) / (85.9 - 81.3)
    elif t <= 89.9:
        return 0.06 + 0.01 * (t - 85.9) / (89.9 - 85.9)
    elif t <= 93.5:
        return 0.07 + 0.01 * (t - 89.9) / (93.5 - 89.9)
    elif t <= 96.7:
        return 0.08 + 0.01 * (t - 93.5) / (96.7 - 93.5)
    elif t <= 99.6:
        return 0.09 + 0.01 * (t - 96.7) / (99.6 - 96.7)
    else:
        # Для температур выше 99.6°C используем интерполяцию по табличным данным (МПа)
        t_table = [100, 110, 120, 130, 140, 150]
        p_table = [0.10133, 0.14327, 0.19854, 0.27011, 0.36136, 0.47597]
        
        if t >= 150:
            # При очень высоких температурах можно экстраполировать, но для учебных целей вернём последнее значение
            return p_table[-1]
        
        # Линейная интерполяция
        for i in range(len(t_table) - 1):
            if t_table[i] <= t <= t_table[i + 1]:
                return p_table[i] + (p_table[i + 1] - p_table[i]) * (t - t_table[i]) / (t_table[i + 1] - t_table[i])
        # Если t оказалось вне интервалов (например, из-за погрешности)
        return p_table[-1]

